// Helper to escape HTML characters
function escapeHtml(text: string): string {
  return text
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;');
}

// Inline Markdown Parser
function parseInline(text: string): string {
  let escaped = escapeHtml(text);

  // Bold **text**
  escaped = escaped.replace(/\*\*(.*?)\*\*/g, '<strong>$1</strong>');

  // Italic *text*
  escaped = escaped.replace(/\*(.*?)\*/g, '<em>$1</em>');

  // Inline code `text`
  escaped = escaped.replace(/`([^`]+)`/g, '<code>$1</code>');

  // Standard links [text](url) — if target ends with .md, convert to .html
  escaped = escaped.replace(/\[([^\]]+)\]\(([^)]+)\)/g, (match, linkText, url) => {
    let target = url;
    // Block dangerous protocols
    if (/^(javascript|data|vbscript):/i.test(target)) target = '#';
    if (target.endsWith('.md')) {
      target = target.slice(0, -3) + '.html';
    }
    return `<a href="${target}">${linkText}</a>`;
  });

  // Wikilinks [[target]] or [[target|custom text]]
  escaped = escaped.replace(/\[\[([^\]]+)\]\]/g, (match, content) => {
    let target = content.trim();
    let linkText = target;

    if (target.includes('|')) {
      const parts = target.split('|');
      target = parts[0].trim();
      linkText = parts[1].trim();
    }

    // Replace slashes with underscores as wiki generator does for filenames
    let href = target.replace(/\//g, '_');
    if (href.endsWith('.md')) {
      href = href.slice(0, -3);
    }
    if (!href.endsWith('.html') && href !== 'index') {
      href += '.html';
    } else if (href === 'index') {
      href = 'index.html';
    }

    return `<a href="${href}">${linkText}</a>`;
  });

  // Images ![alt](url)
  escaped = escaped.replace(/!\[([^\]]*)\]\(([^)]+)\)/g, '<img src="$2" alt="$1">');

  return escaped;
}

/**
 * Sanitize Mermaid diagram source to fix common syntax issues.
 * - Replaces @ in labels (conflicts with Mermaid directives)
 * - Renames reserved keywords used as node IDs
 * - Replaces <> angle brackets in labels (interpreted as HTML)
 */
function sanitizeMermaid(src: string): string {
  let out = src;

  // Reserved keywords that cannot be used as node IDs
  const RESERVED = /^(graph|flowchart|subgraph|end|class|click|style|linkStyle|interpolate|init|beta|accTitle|accDescr|\|)$/i;

  // Replace @ in node IDs with _at_ (Mermaid treats @ as directive prefix)
  // Match @word at start of line (after optional whitespace) followed by [ or -->
  out = out.replace(/^(\s*)@([\w-]+)/gm, (match, indent, name) => {
    return `${indent}_at_${name}`;
  });
  // Also replace @ in edge targets: --> @node or --> @node[
  out = out.replace(/-->(?:\|[^|]*\|)?\s*@([\w-]+)/g, (match, name) => {
    return match.replace(`@${name}`, `_at_${name}`);
  });

  // Replace @ in label text with → (Mermaid treats @ as directive prefix)
  out = out.replace(/\["(.*?)"\]/g, (match, label) => {
    const fixed = label.replace(/@/g, '→ ');
    return `["${fixed}"]`;
  });

  // Replace <> angle brackets in label text with [] (Mermaid treats <> as HTML)
  out = out.replace(/\["(.*?)"\]/g, (match, label) => {
    const fixed = label.replace(/</g, '[').replace(/>/g, ']');
    return `["${fixed}"]`;
  });

  // Rename reserved keyword node IDs by appending -node
  out = out.replace(/^(\s*)(\S+)(\[)/gm, (match, indent, nodeId, bracket) => {
    if (RESERVED.test(nodeId)) {
      return `${indent}${nodeId}-node${bracket}`;
    }
    return match;
  });

  // Fix edge references to renamed reserved node IDs
  out = out.replace(/-->.*/gm, (edgeLine) => {
    return edgeLine.replace(/\|(\s*\S+\s*)\|/g, (labelMatch, label) => labelMatch);
  });

  return out;
}

// Block Markdown Parser
function parseMarkdown(md: string): string {
  let html = '';
  const lines = md.split(/\r?\n/);
  let inCode = false;
  let codeLang = '';
  let codeLines: string[] = [];
  let inList = false;
  let listType: 'ul' | 'ol' | null = null;
  let inBlockquote = false;
  let inTable = false;
  let tableHeaders: string[] = [];
  let tableRows: string[][] = [];

  function closeList() {
    if (inList) {
      html += listType === 'ul' ? '</ul>\n' : '</ol>\n';
      inList = false;
      listType = null;
    }
  }

  function closeBlockquote() {
    if (inBlockquote) {
      html += '</blockquote>\n';
      inBlockquote = false;
    }
  }

  function closeTable() {
    if (inTable) {
      html += '<table>\n<thead>\n<tr>\n';
      for (const h of tableHeaders) {
        html += `<th>${parseInline(h)}</th>\n`;
      }
      html += '</tr>\n</thead>\n<tbody>\n';
      for (const r of tableRows) {
        html += '<tr>\n';
        for (const cell of r) {
          html += `<td>${parseInline(cell)}</td>\n`;
        }
        html += '</tr>\n';
      }
      html += '</tbody>\n</table>\n';
      inTable = false;
      tableHeaders = [];
      tableRows = [];
    }
  }

  function closeBlocks() {
    closeList();
    closeBlockquote();
    closeTable();
  }

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];

    // Code block check
    if (line.trim().startsWith('```')) {
      if (inCode) {
        inCode = false;
        const codeText = codeLines.join('\n');
        if (codeLang === 'mermaid') {
          html += `<div class="mermaid-container">\n<div class="mermaid">\n${escapeHtml(sanitizeMermaid(codeText))}\n</div>\n</div>\n`;
        } else {
          html += `<pre><code class="language-${escapeHtml(codeLang)}">${escapeHtml(codeText)}</code></pre>\n`;
        }
        codeLines = [];
        codeLang = '';
      } else {
        closeBlocks();
        inCode = true;
        codeLang = line.trim().slice(3).trim();
      }
      continue;
    }

    if (inCode) {
      codeLines.push(line);
      continue;
    }

    // Blank line
    if (line.trim() === '') {
      closeBlocks();
      continue;
    }

    // Blockquote
    const bqMatch = line.match(/^\s*>\s?(.*)$/);
    if (bqMatch) {
      if (!inBlockquote) {
        closeBlocks();
        html += '<blockquote>\n';
        inBlockquote = true;
      }
      html += `${parseInline(bqMatch[1])}<br>\n`;
      continue;
    } else {
      closeBlockquote();
    }

    // Table rows
    const isTableRow = line.trim().startsWith('|') && line.trim().endsWith('|');
    if (isTableRow) {
      closeList();
      const cells = line.split('|').map(c => c.trim()).slice(1, -1);
      if (!inTable) {
        // Look ahead to check for separator row
        const nextLine = lines[i + 1];
        if (nextLine && nextLine.trim().startsWith('|') && nextLine.includes('---')) {
          inTable = true;
          tableHeaders = cells;
          i++; // skip separator
        } else {
          closeTable();
          html += `<p>${parseInline(line)}</p>\n`;
        }
      } else {
        tableRows.push(cells);
      }
      continue;
    } else {
      closeTable();
    }

    // Headings
    const hMatch = line.match(/^(#{1,6})\s+(.*)$/);
    if (hMatch) {
      closeBlocks();
      const level = hMatch[1].length;
      html += `<h${level}>${parseInline(hMatch[2])}</h${level}>\n`;
      continue;
    }

    // Unordered List
    const ulMatch = line.match(/^\s*[-*+]\s+(.*)$/);
    if (ulMatch) {
      if (!inList || listType !== 'ul') {
        closeBlocks();
        html += '<ul>\n';
        inList = true;
        listType = 'ul';
      }
      html += `<li>${parseInline(ulMatch[1])}</li>\n`;
      continue;
    }

    // Ordered List
    const olMatch = line.match(/^\s*(\d+)\.\s+(.*)$/);
    if (olMatch) {
      if (!inList || listType !== 'ol') {
        closeBlocks();
        html += '<ol>\n';
        inList = true;
        listType = 'ol';
      }
      html += `<li>${parseInline(olMatch[2])}</li>\n`;
      continue;
    }

    // Horizontal rule
    if (line.match(/^\s*([-*_])\1\1+\s*$/)) {
      closeBlocks();
      html += '<hr>\n';
      continue;
    }

    // Paragraph
    closeBlocks();
    html += `<p>${parseInline(line)}</p>\n`;
  }

  closeBlocks();
  return html;
}

// Extract first H1 or fall back to title-cased filename
function getMarkdownInfo(filePath: string, baseName: string): { title: string } {
  let title = '';
  try {
    const content = fs.readFileSync(filePath, 'utf-8');
    const lines = content.split(/\r?\n/).slice(0, 15);
    for (const line of lines) {
      const match = line.match(/^#\s+(.+)$/);
      if (match) {
        title = match[1].trim();
        break;
      }
    }
  } catch {
    // Ignore
  }
  if (!title) {
    let name = baseName.replace(/^\d+[\s._-]*/, '').replace(/[\s_-]+/g, ' ');
    title = name.replace(/\b\w/g, c => c.toUpperCase());
  }
  return { title };
}

// Build table of contents for a page
function generateTOC(html: string): { tocHTML: string; bodyHTML: string } {
  let bodyHTML = html;
  const tocItems: string[] = [];
  let headerIndex = 0;

  bodyHTML = bodyHTML.replace(/<(h[23]) id="([^"]+)"/gi, (match, tag, existingId) => {
    return match;
  });

  // Inject IDs and build list
  bodyHTML = bodyHTML.replace(/<(h[23])>(.*?)<\/\1>/gi, (match, tag, titleText) => {
    const cleanText = titleText.replace(/<[^>]*>/g, '').trim();
    const id = 'header-' + cleanText.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/(^-|-$)/g, '') + '-' + headerIndex++;
    tocItems.push(`<li class="toc-item ${tag}"><a href="#${id}">${titleText}</a></li>`);
    return `<${tag} id="${id}">${titleText}</${tag}>`;
  });

  if (tocItems.length === 0) {
    return { tocHTML: '', bodyHTML };
  }

  const tocHTML = `
    <div class="toc-container">
      <div class="toc-title">On this page</div>
      <ul class="toc-list">
        ${tocItems.join('\n')}
      </ul>
    </div>
  `;
  return { tocHTML, bodyHTML };
}

// Build the sidebar navigation list
function buildNavigationMenu(
  sourceDir: string,
  menuConfig: string,
  activeBaseName: string,
  menuItemsList: { title: string; url: string }[]
): string {
  const getActiveCls = (href: string) => {
    return href === `${activeBaseName}.html` ? ' class="active"' : '';
  };

  const htmlItems: string[] = [];

  for (const item of menuItemsList) {
    htmlItems.push(`<li><a href="${item.url}"${getActiveCls(item.url.slice(0, -5))}>${escapeHtml(item.title)}</a></li>`);
  }

  return htmlItems.join('\n');
}

// Create a menu.json file
function createMenuJson(sourceDir: string, outputPath: string): void {
  const menuItems: { title: string; url: string }[] = [];

  const indexExists = fs.existsSync(path.join(sourceDir, 'index.md'));
  if (indexExists) {
    menuItems.push({ title: 'Home', url: 'index.html' });
  }

  const readmeExists = fs.existsSync(path.join(sourceDir, 'README.md'));
  if (readmeExists && !indexExists) {
    menuItems.push({ title: 'README', url: 'README.html' });
  }

  const files = fs.readdirSync(sourceDir)
    .filter(f => f.endsWith('.md') && f !== 'index.md' && f !== 'README.md')
    .sort();

  for (const file of files) {
    const fullPath = path.join(sourceDir, file);
    const baseName = path.basename(file, '.md');
    const info = getMarkdownInfo(fullPath, baseName);
    menuItems.push({ title: info.title, url: `${baseName}.html` });
  }

  fs.writeFileSync(outputPath, JSON.stringify(menuItems, null, 2), 'utf-8');
  process.stdout.write(`Sample menu JSON file created successfully at ${outputPath}\n`);
}

