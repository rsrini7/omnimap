export async function commandWiki2Html(args: string[]): Promise<void> {
  let srcDir = process.cwd();
  let outDir = path.join(process.cwd(), 'html');
  
  // Resolve default project title from package.json or directory name
  let defaultTitle = 'Documentation';
  try {
    const pkgPath = path.join(process.cwd(), 'package.json');
    if (fs.existsSync(pkgPath)) {
      const pkg = JSON.parse(fs.readFileSync(pkgPath, 'utf-8'));
      if (pkg.name) {
        defaultTitle = pkg.name
          .replace(/[-_]+/g, ' ')
          .replace(/\b\w/g, (c: string) => c.toUpperCase());
      }
    } else {
      defaultTitle = path.basename(process.cwd())
        .replace(/[-_]+/g, ' ')
        .replace(/\b\w/g, (c: string) => c.toUpperCase());
    }
  } catch {
    // fallback
  }

  let title = defaultTitle;
  let menuConfig = 'auto';
  let primaryColor = '#6366f1';
  let openBrowser = true;
  let createMenu = false;
  let menuJsonPath = path.join(process.cwd(), 'menu.json');
  let help = false;

  for (let i = 0; i < args.length; i++) {
    const arg = args[i];
    if (arg === '--src' && args[i + 1]) srcDir = args[++i];
    else if (arg === '--out' && args[i + 1]) outDir = args[++i];
    else if (arg === '--title' && args[i + 1]) title = args[++i];
    else if (arg === '--menu' && args[i + 1]) menuConfig = args[++i];
    else if (arg === '--color' && args[i + 1]) primaryColor = args[++i];
    else if (arg === '--no-open') openBrowser = false;
    else if (arg === '--create-menu') createMenu = true;
    else if (arg === '--menu-path' && args[i + 1]) menuJsonPath = args[++i];
    else if (arg === '--help' || arg === '-h') help = true;
    else if (!arg.startsWith('-')) srcDir = arg; // positional: source directory
  }

  if (args.length === 0 || help) {
    process.stdout.write(HELP.trim() + '\n');
    return;
  }

  if (primaryColor && !/^#[0-9a-fA-F]{3,8}$/.test(primaryColor)) {
    process.stderr.write('error: --color must be a valid hex color (e.g. #6366f1)\n');
    process.exit(1);
  }

  if (!fs.existsSync(srcDir) || !fs.statSync(srcDir).isDirectory()) {
    process.stderr.write(`error: Source directory "${srcDir}" does not exist or is not a directory.\n`);
    process.exit(1);
  }

  if (createMenu) {
    createMenuJson(srcDir, menuJsonPath);
    return;
  }

  const mdFiles = fs.readdirSync(srcDir)
    .filter(f => f.endsWith('.md'))
    .sort();

  if (mdFiles.length === 0) {
    process.stderr.write(`error: No Markdown (.md) files found in "${srcDir}"\n`);
    process.exit(1);
  }

  // Create the output directory if it doesn't exist
  if (!fs.existsSync(outDir)) {
    fs.mkdirSync(outDir, { recursive: true });
    process.stdout.write(`Created output directory: ${outDir}\n`);
  }

  // Load menu items mapping first
  const menuItemsList: { title: string; url: string }[] = [];

  if (menuConfig === 'auto' || !menuConfig) {
    const indexExists = fs.existsSync(path.join(srcDir, 'index.md'));
    const readmeExists = fs.existsSync(path.join(srcDir, 'README.md'));
    
    if (indexExists) {
      menuItemsList.push({ title: 'Home', url: 'index.html' });
    } else if (readmeExists) {
      menuItemsList.push({ title: 'README', url: 'README.html' });
    }

    const otherFiles = mdFiles.filter(f => f !== 'index.md' && f !== 'README.md');
    for (const file of otherFiles) {
      const fullPath = path.join(srcDir, file);
      const baseName = path.basename(file, '.md');
      const info = getMarkdownInfo(fullPath, baseName);
      menuItemsList.push({ title: info.title, url: `${baseName}.html` });
    }
  } else if (menuConfig !== 'none') {
    try {
      const jsonPath = path.resolve(menuConfig);
      if (fs.existsSync(jsonPath)) {
        const content = fs.readFileSync(jsonPath, 'utf-8');
        const customMenu = JSON.parse(content);
        if (Array.isArray(customMenu)) {
          for (const item of customMenu) {
            menuItemsList.push({ title: item.title, url: item.url });
          }
        }
      }
    } catch (err: any) {
      process.stdout.write(`Warning: Failed to load menu config json: ${err.message}. Falling back to 'auto'.\n`);
      menuConfig = 'auto';
    }
  } else {
    // none - just add index.html/README.html if exist
    const indexExists = fs.existsSync(path.join(srcDir, 'index.md'));
    const readmeExists = fs.existsSync(path.join(srcDir, 'README.md'));
    if (indexExists) {
      menuItemsList.push({ title: 'Home', url: 'index.html' });
    } else if (readmeExists) {
      menuItemsList.push({ title: 'README', url: 'README.html' });
    }
  }

  // Resolve Home Link URL
  const indexExists = fs.existsSync(path.join(srcDir, 'index.md'));
  const homeLinkUrl = indexExists ? 'index.html' : 'README.html';

  // Pre-process and render Markdown content to compile the Search Index
  const searchIndex: { title: string; url: string; content: string }[] = [];
  const processedFiles: {
    fileName: string;
    baseName: string;
    title: string;
    bodyHTML: string;
    tocHTML: string;
  }[] = [];

  for (const file of mdFiles) {
    const fullPath = path.join(srcDir, file);
    const baseName = path.basename(file, '.md');
    const info = getMarkdownInfo(fullPath, baseName);
    
    let rawContent = '';
    try {
      rawContent = fs.readFileSync(fullPath, 'utf-8');
    } catch {
      continue;
    }

    // Strip metadata sections if generated by wiki (e.g. --- \n name: index \n ---)
    let markdownToParse = rawContent;
    if (rawContent.startsWith('---')) {
      const endMeta = rawContent.indexOf('---', 3);
      if (endMeta !== -1) {
        markdownToParse = rawContent.slice(endMeta + 3).trim();
      }
    }

    // Parse Markdown to HTML
    const renderedBody = parseMarkdown(markdownToParse);
    
    // Auto-generate TOC
    const { tocHTML, bodyHTML } = generateTOC(renderedBody);

    // Prepare search index content — strip markdown syntax for clean text
    const textOnly = markdownToParse
      .replace(/```[\s\S]*?```/g, '') // remove code blocks
      .replace(/<[^>]*>/g, '') // remove HTML tags
      .replace(/\[\[([^\]]+?)\]\]/g, (_, t) => t.includes('|') ? t.split('|')[1].trim() : t.trim()) // wikilinks → text
      .replace(/\[([^\]]+)\]\([^)]+\)/g, '$1') // [text](url) → text
      .replace(/!\[[^\]]*\]\([^)]+\)/g, '') // images
      .replace(/^#{1,6}\s+/gm, '') // heading markers
      .replace(/\*\*(.*?)\*\*/g, '$1') // bold
      .replace(/\*(.*?)\*/g, '$1') // italic
      .replace(/`([^`]+)`/g, '$1') // inline code
      .replace(/^[-*+]\s+/gm, '') // list markers
      .replace(/^\d+\.\s+/gm, '') // ordered list markers
      .replace(/^>\s?/gm, '') // blockquotes
      .replace(/\|/g, ' ') // table pipes
      .replace(/---+/g, ' ') // horizontal rules
      .replace(/\r?\n/g, ' ') // normalize whitespace
      .replace(/\s+/g, ' ')
      .trim();

    const outputHtmlFileName = baseName === 'index' ? 'index.html' : `${baseName}.html`;
    searchIndex.push({
      title: info.title,
      url: outputHtmlFileName,
      content: textOnly
    });

    processedFiles.push({
      fileName: file,
      baseName,
      title: info.title,
      bodyHTML,
      tocHTML
    });
  }

  // Write the search index file (CORS-safe global variable)
  const searchIndexJsContent = `const WIKI_SEARCH_INDEX = ${JSON.stringify(searchIndex, null, 2)};`;
  fs.writeFileSync(path.join(outDir, 'search-index.js'), searchIndexJsContent, 'utf-8');

  // Process and write HTML files
  for (let idx = 0; idx < processedFiles.length; idx++) {
    const file = processedFiles[idx];
    const outputHtmlFileName = file.baseName === 'index' ? 'index.html' : `${file.baseName}.html`;
    const outputFile = path.join(outDir, outputHtmlFileName);

    // Sidebar navigation rendering with active page highlight
    const sidebarHTML = buildNavigationMenu(srcDir, menuConfig, file.baseName, menuItemsList);

    // Previous & Next navigation buttons
    let prevButton = '';
    let nextButton = '';
    
    // Find index of current file in the menuItemsList
    const menuIdx = menuItemsList.findIndex(item => item.url === outputHtmlFileName);
    if (menuIdx !== -1) {
      if (menuIdx > 0) {
        const prevItem = menuItemsList[menuIdx - 1];
        prevButton = `
          <a href="${prevItem.url}" class="pagination-link prev">
            <span class="pagination-label">Previous</span>
            <span class="pagination-title">${escapeHtml(prevItem.title)}</span>
          </a>
        `;
      }
      if (menuIdx < menuItemsList.length - 1) {
        const nextItem = menuItemsList[menuIdx + 1];
        nextButton = `
          <a href="${nextItem.url}" class="pagination-link next">
            <span class="pagination-label">Next</span>
            <span class="pagination-title">${escapeHtml(nextItem.title)}</span>
          </a>
        `;
      }
    }

    const paginationHTML = `
      <div class="pagination">
        ${prevButton}
        ${nextButton}
      </div>
    `;

    // Inject variable values into the main template — escape HTML in user-provided values
    const safeTitle = escapeHtml(title);
    const safeFileTitle = escapeHtml(file.title);
    const safeColor = primaryColor.replace(/[^#0-9a-fA-F]/g, '');
    const finalHTML = HTML_TEMPLATE
      .replace(/\$title\$/g, `${safeTitle} - ${safeFileTitle}`)
      .replace(/PRIMARYCOLOR/g, safeColor)
      .replace(/\$ProjectTitle\$/g, safeTitle)
      .replace(/HOME_LINK_URL/g, homeLinkUrl)
      .replace(/NAVIGATION/g, sidebarHTML)
      .replace(/TOC/g, file.tocHTML)
      .replace(/BODY/g, file.bodyHTML)
      .replace(/PAGINATION/g, paginationHTML);

    fs.writeFileSync(outputFile, finalHTML, 'utf-8');
  }

  process.stdout.write(`[SUCCESS] Documentation generated successfully!\n`);
  process.stdout.write(`[SUCCESS] HTML files are in ${outDir} (${processedFiles.length} pages generated)\n`);

  if (openBrowser) {
    let indexPath = path.join(outDir, 'index.html');
    if (!fs.existsSync(indexPath)) {
      indexPath = path.join(outDir, 'README.html');
    }

    if (fs.existsSync(indexPath)) {
      process.stdout.write(`Opening ${indexPath} in your default browser...\n`);
      const startCmd = process.platform === 'darwin' ? 'open' : process.platform === 'win32' ? 'start' : 'xdg-open';
      execFile(startCmd, [indexPath], (err) => {
        if (err) {
          process.stdout.write(`Warning: Could not open browser automatically. Please open the file manually: ${indexPath}\n`);
        }
      });
    }
  }
}
